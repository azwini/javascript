// ES6: Use Destructuring Assignment to Assign Variables from Arrays

// initializing `a` & `b` with `8` & `6` respectively
let a = 8, b = 6;

// swapping the variable values
[a, b] = [b, a];

console.log(a, b); // prints `6` & `8`
