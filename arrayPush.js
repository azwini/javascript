// Basic JavaScript: Manipulate Arrays With push()

// `push` will append data to the end of array

// passing array items to variable myArray
var myArray = [
    ["John", 23],
    ["cat", 2]
];

// `.push` operation is done on myArray
myArray.push(["dog", 3]);

// `[["John", 23], ["cat", 2], ["dog", 3]]`
console.log(myArray);
