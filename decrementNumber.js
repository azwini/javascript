// Basic JavaScript: Decrement a Number with JavaScript


var myVar = 11; // intialize `myVar` with `11`
myVar--;
/**
 * `--` is decrement operator
 * myVar-- is equal to --> myVar = myVar - 1;
 */
console.log(myVar); // `10`
