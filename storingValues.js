// Basic JavaScript: Storing Values with the Assignment Operator


// declare variable `a`
var a;
// declare variable `b`
var b;
// passing 7 to `a`
a = 7;
// assigning `b` with `a` value
b = a;
console.log(b); // prints `b` with value `7`
console.log(a); // prints `a` with value `7`
