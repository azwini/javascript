// Basic JavaScript: Increment a Number with JavaScript


var myVar = 87; // intialize `myVar` with `87`
myVar++;
/**
 * `++` is increment operator
 * myVar++ is equal to --> myVar = myVar + 1;
 * hence eliminating `=`
 */
console.log(myVar); // `88`
