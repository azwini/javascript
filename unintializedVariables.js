// Basic JavaScript: Understanding Uninitialized Variables


var a = 5; // initialize variable `a` with value `5`
var b = 10; // initialize variable `b` with value `10`
var c = "I am a"; // initialize variable `c` with string `I am a`

a = a + 1; // defining `a`
b = b + 5; // defining `b`
c = c + " String!"; // defining `c`

console.log(a); // `6`
console.log(b); // `15`
console.log(c); // `I am a String!`
